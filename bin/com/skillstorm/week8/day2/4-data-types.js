/**
 * Primitives:
 * - String
 * - Number
 * - Boolean
 * - Null
 * - Undefined
 * - BigInt
 * - Symbol
 * 
 * Non-Primitives:
 * - Objects (Object, Array)
 */

// typeof keyword allows us to check the type of a value at runtime
console.log(typeof 1); // number
console.log(typeof '1'); // string
console.log(typeof {}); // object
console.log(typeof []); // object

