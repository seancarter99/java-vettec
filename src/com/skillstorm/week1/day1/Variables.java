package com.skillstorm.week1.day1;

public class Variables {
	
	public static void main(String[] args) {
		int ten;
		ten = 10;
		
		/*
		 * "Java is strongly typed"
		 * Each variable must have a type specified
		 * 
		 * type - Data type
		 * 
		 * Ensures "type safety". Ensures that we're always the type specified
		 */
		
		String msg; // Declaring a variable
		
		// "Declaring a variable" - Simply stating the type and name of the variable
		
		/*
		 * Doing some algrorithmic process
		 * 
		 * 50 lines later...
		 */
		msg = "John";
		
		// Type variable-name
		String sam; // Variable declaration
		sam = "Sam"; // Variable assignment
		// This sets the value of word to be an empty string
		
		String john = "John"; // Variable declaration and assignment
		
		
		System.out.println(sam);
		System.out.println(john);
		
		// Camel case (Same as pascal case, but the first letter is lower cased)
		
		
		// Numbers
		
		int num = 40;
		
		num = 42;
		num = 32;
		
		System.out.println(num);
//		num = "fsd"; // Can't assign to be a String
		
		/*
		 * Data types:
		 * 
		 * - int - integer (negative and non-negative whole numbers)
		 * - String - Sequence of characters (words, sentences, paragraphs, a letter)
		 * - boolean - True/False
		 * - double - Decimals (40.56)
		 * - char - Single letter or character (ex. a, A, g, _, ?, etc.)
		 * 
		 * --- Others ----
		 * - float - Floating point (similar to a double, but less precise)
		 * - long - Bigger integer
		 * - short -  Smaller integer
		 * - byte - 8 bit number, (Tiny)
		 */
		
		// Primitives vs Non-primitives
		
		char middleInitial = 'P';
		String firstName = "Sean"; // Sean -> Shawn
		
	}
}
